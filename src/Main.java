public class Main {
    public static void main(String[] args) {
        Color red = Color.RED;
        Color blue = Color.BLUE;
        Color green = Color.GREEN;

        // Red color
        System.out.println("Color " + red);
        System.out.println("Hex code: " + red.getHexCode());

        // Blue color
        System.out.println("Color " + blue);
        System.out.println("Hex code: " + blue.getHexCode());

        // Green color
        System.out.println("Color " + green);
        System.out.println("Hex code: " + green.getHexCode());
    }
}